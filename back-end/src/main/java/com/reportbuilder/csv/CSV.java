package com.reportbuilder.csv;

public class CSV {
    private String data;

    public CSV()
    {
        super();
    }

    public CSV(String data)
    {
        super();
        this.data = data;
    }


    public String getData()
    {
        return this.data;
    }

    public void setData(String newData)
    {
        this.data = newData;
    }



}
